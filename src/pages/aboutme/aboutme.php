            <h1>Interests and Skills</h1>
            <p>
                The world is jam-full of things to be curious about.
                It is my belief that one can self-improve at almost anything by spending time and energy on it.
                Hence, this is a collection of both, things I am enthusiastic about and more or less good at.
            </p>

            <div class="interesting_boxes">

                <div>
                    <p class="title">Scala and Functional Programming</p>
                    <p class="description">
                        There are lots of facts that I love about <a href="https://www.scala-lang.org/">Scala</a>. First
                        and foremost it implements the concepts of
                        <a href="https://en.wikipedia.org/wiki/Functional_programming">Functional Programming</a> which
                        I am an absolute fan of. But also it makes writing concise code easy and I do loathe boilerplate.
                    </p>
                </div>

                <div>
                    <p class="title">Reactive Systems and Streaming Data</p>
                    <p class="description">
                        In my view, Sofware Engineering is still in an early stage of its development.
                        To build modern and reliable software system of the future, in addition to Functional
                        Programming, carefully designed paradigm frameworks like the
                        <a href="https://www.reactivemanifesto.org/">Reactive Manifesto</a>
                        can lead the way.
                    </p>
                </div>

                <div>
                    <p class="title">Bits and Databases</p>
                    <p class="description">
                        If it's true that
                        <a href="https://blog.s4rb.com/data-is-the-oil-of-the-21st-century">data is the new oil of the 21st century</a>,
                        I think it's absolutely necessary to spread knowledge about the basics of
                        <a href="https://en.wikipedia.org/wiki/Information_theory">Information theory</a>,
                        how bits as the atomic unit of information become structured data,
                        <a href="https://en.wikipedia.org/wiki/SQL">SQL</a>,
                        and how databases actually work.
                        After all,
                        <i><a href="https://en.wikipedia.org/wiki/Scientia_potentia_est">scientia potentia est</a>!</i>
                    </p>
                </div>

                <div>
                    <p class="title">Agile Principles and Communication</p>
                    <p class="description">
                        I wholeheartedly dig the <a href="https://agilemanifesto.org/">Agile Manifesto</a>, because I
                        think its strength lies in the fact that it's closer to the way humans think and live than other
                        approaches.
                        Whenever I'm convinced that there is a structural issue, I'd rather spend my time discussing it
                        than risking to waste time in unreasoning labour.
                    </p>
                </div>

                <div>
                    <p class="title">Linux and Free Software</p>
                    <p class="description">
                        I'm convinced that the existence of Free Software makes the world a better place.
                        Not only that I use Linux - the OS that sucks fewest - personally, I think it also is a matter
                        of public interest and support
                        <i><a href="https://publiccode.eu/openletter/">Public Money - Public Code</a></i>.
                    </p>
                </div>

                <div>
                    <p class="title">Vim, Scripting and the CLI</p>
                    <p class="description">
                        Being an absolute Vim addict,
                        <a href="https://github.com/sflip/dotfiles/blob/master/vim/vimrc">my handcrafted .vimrc</a> has
                        over 5000 lines. I love to customize my dev environment - and hate to move my hands away from
                        the keyboard. If I manage to automate another nasty task, it makes me sleep better.
                    </p>
                </div>

                <div>
                    <p class="title">Mathematics and Philosophy</p>
                    <p class="description">
                        It fascinates me how intermingled different fields as mathemathics, philosophy and music really
                        are, e.g. the concept of <a href="https://en.wikipedia.org/wiki/Recursion">recursion</a> can be
                        found everywhere.
                        I'm the happiest whenever a new beautiful
                        <a href="https://en.wikipedia.org/wiki/Isomorphism">isomorphism</a>
                        pops up as I am getting closer to what's at the core of cognition and intelligence.
                        One of the best expressions of this idea might be
                        <a href="https://en.wikipedia.org/wiki/G%C3%B6del,_Escher,_Bach">GEB by Douglas Hofstadter</a>.
                    </p>
                </div>

                <div>
                    <p class="title">Science and Critical Rationalism</p>
                    <p class="description">
                        The most important finding of science was the finding of our own ignorance.
                        The Enlightenment is probably one of the best things that happened to mankind. But from
                        <a href="https://en.wikipedia.org/wiki/G%C3%B6del%27s_incompleteness_theorems">Gödel</a> to
                        Einstein proving Newton wrong, history makes me humble and I sympathize with the idea of
                        <a href="https://en.wikipedia.org/wiki/Critical_rationalism">Critical rationalism</a>.
                    </p>
                </div>

                <div>
                    <p class="title">Logical Puzzles and Problem Solving</p>
                    <p class="description">
                        I guess the reason that I enjoy programming so much is that it's like solving a mental puzzle.
                        Nothing beats the satisfaction when one's mind has resolved that state of confusion using logic
                        and creativity to the same extend and turned it into some program that <i>just works</i>.
                    </p>
                </div>

                <div>
                    <p class="title">Listen to and make Music</p>
                    <p class="description">
                        Music is my place of inner peace. From Jazz over Funk and Soul via Electroswing to Hard Rock,
                        I enjoy a broad spectrum - I think of music as I think of food: regularity and diversity matter!
                        People like
                        <a href="https://www.youtube.com/user/jacobcolliermusic">Jacob Collier</a>
                        inspire me.
                        Learning to play the piano when I was young, I am now playing electric bass guitar in
                        <a href="https://www.cantaloupe-jazz.com/wp/">a Big Band</a>
                        and singing in
                        <a href="http://catchatune.de/">a Jazz choir</a>.
                    </p>
                </div>

                <div>
                    <p class="title">Table tennis and Rock Climbing</p>
                    <p class="description">
                        Table tennis is the sport I grew up with. I love the precision and competition and only plan to
                        retire when I die.
                        Many years ago now, I also discovered rock climbing. The holistic requirements fascinate me and
                        I can really clear my mind being on the wall.
                    </p>
                </div>

                <div>
                    <p class="title">Poker and Board Games</p>
                    <p class="description">
                        Even though I tend to like gambling sometimes, I believe poker isn't about gambling at all.
                        To me, it's just the perfect symbiosis of statistics and psychology. Also it teaches my a lot
                        from the mechanics of capitalism to being a good sport when losing.
                    </p>
                </div>

